def relational_operations(num1, num2):
    print(f"Number 1: {num1}")
    print(f"Number 2: {num2}")
    print(f"{num1} == {num2}: {num1 == num2}")
    print(f"{num1} != {num2}: {num1 != num2}")
    print(f"{num1} > {num2}: {num1 > num2}")
    print(f"{num1} < {num2}: {num1 < num2}")
    print(f"{num1} >= {num2}: {num1 >= num2}")
    print(f"{num1} <= {num2}: {num1 <= num2}")
integer1 = 1234567890
integer2 = 9876543
relational_operations(integer1, integer2)
